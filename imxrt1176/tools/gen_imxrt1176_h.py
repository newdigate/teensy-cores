#!/usr/bin/env python3
"""Generate a standalone Teensy-idiom imxrt1176.h from the NXP CMSIS header.

Emits `#define <PREFIX>_<REG> (*(volatile uint32_t *)0x...)` for the peripherals
Phase 0 needs.  Reads peripheral base addresses from the NXP MIMXRT1176_cm7_COMMON.h
`#define <INST>_BASE (...)` lines.  Output has no SDK dependency.

NOTE: Base addresses are in MIMXRT1176_cm7_COMMON.h (not the top-level
MIMXRT1176_cm7.h which only has peripheral-struct #includes).
"""
import re, sys, pathlib

SDK = pathlib.Path.home() / "Development/nxp/mcux-devices-rt/RT1170/MIMXRT1176/MIMXRT1176_cm7_COMMON.h"
OUT = pathlib.Path(__file__).resolve().parent.parent / "imxrt1176.h"

WANTED = {"CCM": "CCM", "IOMUXC": "IOMUXC", "IOMUXC_GPR": "IOMUXC_GPR",
          "FLEXSPI1": "FLEXSPI", "FLEXSPI2": "FLEXSPI2",
          "ANADIG_PLL": "ANADIG_PLL", "ANADIG_OSC": "ANADIG_OSC",
          **{f"GPIO{i}": f"GPIO{i}" for i in range(1, 14)}}

# --- CCM clock roots (Task 9: 996 MHz core clock tree) -----------------------
# CCM->CLOCK_ROOT[n].CONTROL is at CCM_BASE + n*0x80 (CONTROL at struct offset 0).
# We emit the roots the 996 MHz bring-up touches.  See SDK BOARD_BootClockRUN /
# clock_root_t enum (fsl_clock.h):  M7=0, Bus=2, M7_Systick=8.
CCM_ROOT_STRIDE = 0x80
CCM_CLOCK_ROOTS = {0: "M7", 2: "BUS", 8: "M7_SYSTICK"}

# ANADIG_PLL / ANADIG_OSC register offsets (from MIMXRT1176_cm7.h peripheral
# struct layouts).  Both blocks share base 0x40C84000 in the analog top.
ANADIG_PLL_REGS = {"ARM_PLL_CTRL": 0x200}   # ARM_PLL_CTRL_REGISTER, offset 0x200
ANADIG_OSC_REGS = {"OSC_24M_CTRL": 0x020}   # 24MHz OSC Control,      offset 0x020

# --- IOMUXC per-pad mux / pad-control registers (Task 11: EVKB pin table) -----
# Offsets from IOMUXC_BASE (0x400E8000), derived from the SDK's pin-mux macros
# in fsl_iomuxc.h.  Each macro `IOMUXC_<PAD>_<SIGNAL>` expands to
#   muxRegister, muxMode, inputRegister, inputDaisy, configRegister
# e.g. IOMUXC_GPIO_AD_04_GPIO9_IO03 = 0x400E811C, 0xA, 0, 0, 0x400E8360
# so SW_MUX_CTL_PAD_GPIO_AD_04 @ +0x11C, SW_PAD_CTL_PAD_GPIO_AD_04 @ +0x360.
# muxMode 0xA (ALT10) selects GPIO9_IO03 (the EVKB User LED D6, GPIO9 bit 3).
IOMUXC_MUX_CTL_REGS = {"GPIO_AD_04": 0x11C}
IOMUXC_PAD_CTL_REGS = {"GPIO_AD_04": 0x360}

def parse_bases(txt):
    bases = {}
    for m in re.finditer(r'#define\s+(\w+)_BASE\s+\(([0-9a-fA-Fxu]+)\)', txt):
        try: bases[m.group(1)] = int(m.group(2).rstrip("uU"), 0)
        except ValueError: pass
    return bases

def main():
    bases = parse_bases(SDK.read_text())
    L = ["/* AUTO-GENERATED by tools/gen_imxrt1176_h.py -- do not edit by hand.",
         " * Standalone Teensy-idiom register defs for NXP i.MX RT1176 (CM7). */",
         "#ifndef _imxrt1176_h_", "#define _imxrt1176_h_", "#include <stdint.h>", ""]
    for inst, pfx in sorted(WANTED.items()):
        b = bases.get(inst)
        if b is None: print(f"warn: no base for {inst}", file=sys.stderr); continue
        L.append(f"#define {pfx}_BASE 0x{b:08X}u")
    for i in range(1, 14):
        b = bases.get(f"GPIO{i}")
        if b is None: continue
        L += [f"#define GPIO{i}_DR        (*(volatile uint32_t *)0x{b+0x00:08X}u)",
              f"#define GPIO{i}_GDIR      (*(volatile uint32_t *)0x{b+0x04:08X}u)",
              f"#define GPIO{i}_PSR       (*(volatile uint32_t *)0x{b+0x08:08X}u)",
              f"#define GPIO{i}_DR_SET    (*(volatile uint32_t *)0x{b+0x84:08X}u)",
              f"#define GPIO{i}_DR_CLEAR  (*(volatile uint32_t *)0x{b+0x88:08X}u)",
              f"#define GPIO{i}_DR_TOGGLE (*(volatile uint32_t *)0x{b+0x8C:08X}u)"]
    # --- CCM clock-root CONTROL registers (Task 9) ---------------------------
    ccm = bases.get("CCM")
    if ccm is not None:
        L.append("")
        L.append("/* CCM clock roots: CCM->CLOCK_ROOT[n].CONTROL @ CCM_BASE + n*0x80 */")
        for n, nm in sorted(CCM_CLOCK_ROOTS.items()):
            addr = ccm + n * CCM_ROOT_STRIDE
            L.append(f"#define CCM_CLOCK_ROOT{n}_CONTROL (*(volatile uint32_t *)0x{addr:08X}u) /* {nm} */")
        L += ["#define CCM_CLOCK_ROOT_CONTROL_MUX(x)  (((uint32_t)(x) << 8) & 0x700u)",
              "#define CCM_CLOCK_ROOT_CONTROL_DIV(x)  (((uint32_t)(x) << 0) & 0x0FFu)"]

    # --- ANADIG PLL / OSC registers (Task 9: ARM PLL = 996 MHz, 24M OSC) ------
    apll = bases.get("ANADIG_PLL")
    if apll is not None:
        L.append("")
        for nm, off in sorted(ANADIG_PLL_REGS.items()):
            L.append(f"#define ANADIG_PLL_{nm} (*(volatile uint32_t *)0x{apll+off:08X}u)")
        L += ["#define ANADIG_PLL_ARM_PLL_CTRL_DIV_SELECT(x)   (((uint32_t)(x) << 0) & 0x000000FFu)",
              "#define ANADIG_PLL_ARM_PLL_CTRL_HOLD_RING_OFF   (1u << 12)",
              "#define ANADIG_PLL_ARM_PLL_CTRL_POWERUP         (1u << 13)",
              "#define ANADIG_PLL_ARM_PLL_CTRL_ENABLE_CLK      (1u << 14)",
              "#define ANADIG_PLL_ARM_PLL_CTRL_POST_DIV_SEL(x) (((uint32_t)(x) << 15) & 0x00018000u)",
              "#define ANADIG_PLL_ARM_PLL_CTRL_BYPASS          (1u << 17)",
              "#define ANADIG_PLL_ARM_PLL_CTRL_ARM_PLL_STABLE  (1u << 29)",
              "#define ANADIG_PLL_ARM_PLL_CTRL_ARM_PLL_GATE    (1u << 30)"]
    aosc = bases.get("ANADIG_OSC")
    if aosc is not None:
        for nm, off in sorted(ANADIG_OSC_REGS.items()):
            L.append(f"#define ANADIG_OSC_{nm} (*(volatile uint32_t *)0x{aosc+off:08X}u)")
        L += ["#define ANADIG_OSC_OSC_24M_CTRL_OSC_EN          (1u << 4)",
              "#define ANADIG_OSC_OSC_24M_CTRL_LP_EN           (1u << 2)",
              "#define ANADIG_OSC_OSC_24M_CTRL_OSC_24M_STABLE  (1u << 30)"]

    # --- IOMUXC per-pad mux / pad-ctl registers (Task 11) --------------------
    iomuxc = bases.get("IOMUXC")
    if iomuxc is not None and (IOMUXC_MUX_CTL_REGS or IOMUXC_PAD_CTL_REGS):
        L.append("")
        L.append("/* IOMUXC per-pad mux/pad-control regs (offsets from fsl_iomuxc.h) */")
        for pad, off in sorted(IOMUXC_MUX_CTL_REGS.items()):
            L.append(f"#define IOMUXC_SW_MUX_CTL_PAD_{pad} (*(volatile uint32_t *)0x{iomuxc+off:08X}u)")
        for pad, off in sorted(IOMUXC_PAD_CTL_REGS.items()):
            L.append(f"#define IOMUXC_SW_PAD_CTL_PAD_{pad} (*(volatile uint32_t *)0x{iomuxc+off:08X}u)")

    L += ["", "#define SYST_CSR   (*(volatile uint32_t *)0xE000E010u)",
          "#define SYST_RVR   (*(volatile uint32_t *)0xE000E014u)",
          "#define SYST_CVR   (*(volatile uint32_t *)0xE000E018u)",
          "#define SCB_VTOR   (*(volatile uint32_t *)0xE000ED08u)",
          "#define SCB_ICSR   (*(volatile uint32_t *)0xE000ED04u)",
          "#define SCB_ICSR_PENDSTSET (1u << 26)",
          "#define ARM_DWT_CYCCNT (*(volatile uint32_t *)0xE0001004u)",
          "#define ARM_DWT_CTRL   (*(volatile uint32_t *)0xE0001000u)",
          "#define ARM_DEMCR      (*(volatile uint32_t *)0xE000EDFCu)",
          "#define ARM_DEMCR_TRCENA (1u << 24)"]
    L += ["",
          "/* --- Cortex-M7 NVIC (Task: interrupt infrastructure) --- */",
          "#define NVIC_ISER(n) (*(volatile uint32_t *)(0xE000E100u + ((n) << 2)))",
          "#define NVIC_ICER(n) (*(volatile uint32_t *)(0xE000E180u + ((n) << 2)))",
          "#define NVIC_ISPR(n) (*(volatile uint32_t *)(0xE000E200u + ((n) << 2)))",
          "#define NVIC_ICPR(n) (*(volatile uint32_t *)(0xE000E280u + ((n) << 2)))",
          "#define NVIC_IP(n)   (*(volatile uint8_t  *)(0xE000E400u + (n)))",
          "#define NVIC_ENABLE_IRQ(n)   (NVIC_ISER((n) >> 5) = (1u << ((n) & 31)))",
          "#define NVIC_DISABLE_IRQ(n)  (NVIC_ICER((n) >> 5) = (1u << ((n) & 31)))",
          "#define NVIC_SET_PENDING(n)  (NVIC_ISPR((n) >> 5) = (1u << ((n) & 31)))",
          "#define NVIC_CLEAR_PENDING(n)(NVIC_ICPR((n) >> 5) = (1u << ((n) & 31)))",
          "#define NVIC_SET_PRIORITY(n, p) (NVIC_IP(n) = (uint8_t)(p))"]
    L += ["", "#endif"]
    OUT.write_text("\n".join(L) + "\n")
    print(f"wrote {OUT}")

if __name__ == "__main__": main()
